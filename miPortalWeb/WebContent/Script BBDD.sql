DROP DATABASE IF EXISTS miPortal;
CREATE DATABASE miPortal;
use miPortal;

CREATE TABLE directores
(
idDirector int PRIMARY KEY AUTO_INCREMENT,
nombre varchar(255),
fechaNacimiento varchar(255)
) ENGINE=InnoDB;

CREATE TABLE usuarios
(
idUsuario int PRIMARY KEY AUTO_INCREMENT,
nick varchar(255),
password varchar(255),
City varchar(255) 
) ENGINE=InnoDB;

CREATE TABLE generos
(
idGenero int PRIMARY KEY AUTO_INCREMENT,
descripcion varchar(255)
) ENGINE=InnoDB;

CREATE TABLE actores
(
idActor int PRIMARY KEY AUTO_INCREMENT,
nombre varchar(255),
fechaNacimiento varchar(255)
) ENGINE=InnoDB;


CREATE TABLE peliculas
(
idPelicula int PRIMARY KEY AUTO_INCREMENT,
titulo varchar(255) NOT NULL,
idGenero int,
idDirector int,
CONSTRAINT `GENERO_PELICULA` FOREIGN KEY (`idGenero`) REFERENCES `generos` (`idGenero`),
CONSTRAINT `DIRECTOR_PELICULA` FOREIGN KEY (`idDirector`) REFERENCES `directores` (`idDirector`)
) ENGINE=InnoDB;

CREATE TABLE series
(
idSerie int PRIMARY KEY AUTO_INCREMENT,
titulo varchar(255) NOT NULL,
idGenero int,
idDirector int,
CONSTRAINT `SERIE_GENERO` FOREIGN KEY (`idGenero`) REFERENCES `generos` (`idGenero`),
CONSTRAINT `DIRECTOR_SERIE` FOREIGN KEY (`idDirector`) REFERENCES `directores` (`idDirector`)
) ENGINE=InnoDB;

CREATE TABLE actores_serie
(
id int PRIMARY KEY AUTO_INCREMENT,
idActor int,
idSerie int,
CONSTRAINT `ACTOR_SERIE` FOREIGN KEY (`idActor`) REFERENCES `actores` (`idActor`),
CONSTRAINT `SERIE_ACTOR` FOREIGN KEY (`idSerie`) REFERENCES `series` (`idSerie`)
) ENGINE=InnoDB;

CREATE TABLE actores_peliculas
(
id int PRIMARY KEY AUTO_INCREMENT,
idActor int,
idPelicula int,
CONSTRAINT `ACTOR_PELICULA` FOREIGN KEY (`idActor`) REFERENCES `actores` (`idActor`),
CONSTRAINT `PELICULA_SERIE` FOREIGN KEY (`idPelicula`) REFERENCES `peliculas` (`idPelicula`)
) ENGINE=InnoDB;

CREATE TABLE valoraciones_series
(
id int PRIMARY KEY AUTO_INCREMENT,
idSerie int,
idUsuario int,
comentario text,
votacion int,
CONSTRAINT `USUARIO_VALORA_SERIE` FOREIGN KEY (`idUsuario`) REFERENCES `usuarios` (`idUsuario`),
CONSTRAINT `SERIE_VALORADA` FOREIGN KEY (`idSerie`) REFERENCES `series` (`idSerie`)
) ENGINE=InnoDB;

CREATE TABLE valoraciones_peliculas
(
id int PRIMARY KEY AUTO_INCREMENT,
idPelicula int,
idUsuario int,
comentario text,
votacion int,
CONSTRAINT `USUARIO_VALORA_PELICULA` FOREIGN KEY (`idUsuario`) REFERENCES `usuarios` (`idUsuario`),
CONSTRAINT `PELICULA_VALORADA` FOREIGN KEY (`idPelicula`) REFERENCES `peliculas` (`idPelicula`)
) ENGINE=InnoDB;

